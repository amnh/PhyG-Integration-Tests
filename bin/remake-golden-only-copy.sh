#!/bin/sh

# Shell script to update golden test files

for SUBDIRECTORY in *
do
    echo $SUBDIRECTORY
    rm $SUBDIRECTORY/\*.csv.golden
    rm $SUBDIRECTORY/\*.tre.golden
    cp $SUBDIRECTORY/$SUBDIRECTORY.tre $SUBDIRECTORY/$SUBDIRECTORY.tre.golden
    cp $SUBDIRECTORY/$SUBDIRECTORY'_gv.tre' $SUBDIRECTORY/$SUBDIRECTORY'_gv.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_threshold-10.tre' $SUBDIRECTORY/$SUBDIRECTORY'_threshold-10.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_threshold-1.tre' $SUBDIRECTORY/$SUBDIRECTORY'_threshold-1.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_threshold-05.tre' $SUBDIRECTORY/$SUBDIRECTORY'_threshold-05.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_atrandom.tre' $SUBDIRECTORY/$SUBDIRECTORY'_atrandom.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_unique.tre' $SUBDIRECTORY/$SUBDIRECTORY'_unique.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_best.tre' $SUBDIRECTORY/$SUBDIRECTORY'_best.tre.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY.csv $SUBDIRECTORY/$SUBDIRECTORY.csv.golden
    cp $SUBDIRECTORY/$SUBDIRECTORY'_ia.txt' $SUBDIRECTORY/$SUBDIRECTORY'_ia.txt.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_diag.txt' $SUBDIRECTORY/$SUBDIRECTORY'_diag.txt.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_info.csv' $SUBDIRECTORY/$SUBDIRECTORY'_info.csv.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY'_cr.csv' $SUBDIRECTORY/$SUBDIRECTORY'_cr.csv.golden'
    cp $SUBDIRECTORY/$SUBDIRECTORY.dot $SUBDIRECTORY/$SUBDIRECTORY.dot.golden
    cp $SUBDIRECTORY/$SUBDIRECTORY.ss $SUBDIRECTORY/$SUBDIRECTORY.ss.golden
done
